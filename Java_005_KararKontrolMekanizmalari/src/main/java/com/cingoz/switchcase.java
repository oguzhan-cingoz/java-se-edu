package com.cingoz;

public class switchcase {

    public static void main(String[] args) {

        char harf ='Z';
        switch (harf){
            case 'A':
                System.out.println("Girilen harf:" +harf);
                break;
            case 'B':
                System.out.println("Girilen harf:" +harf);
                break;
            case 'C':
                System.out.println("Girilen harf:" +harf);
                break;

            default:
                System.out.println("Girilen değer bulunamadı: "+harf);

        }

    }

}
